package pts

import (
	"context"

	"github.com/autobrr/upbrr/internal/trackers"
)

func prepareDescription(_ context.Context, req trackers.PreparationInput) (trackers.DescriptionResult, error) {
	assets, err := trackers.PreparedDescriptionAssets(req.Assets)
	if err != nil {
		assets = trackers.DescriptionAssets{}
	}
	description := buildDescription(req.Meta, assets)
	return trackers.DescriptionResult{Group: "pts", Description: description}, nil
}
